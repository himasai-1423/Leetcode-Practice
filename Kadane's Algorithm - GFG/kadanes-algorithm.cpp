class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        // Your code here
        int maxVal = INT_MIN, currentMax = 0; 
        for(int i=0; i<n; i++){
            currentMax += arr[i];
            if(currentMax>maxVal)
                maxVal = currentMax;
            
            if(currentMax<0)
                currentMax = 0;
        }
        return maxVal;
    }
};

